// Converts the bundled Coin Metrics CSV (2010-07-18 .. 2017-08-16 daily BTC
// closes) into a TypeScript module shipped with the btc-rainbow-chart
// registry item. Run: node scripts/generate-btc-seed.mjs
import { readFileSync, writeFileSync } from "node:fs";

const CSV_PATH = "scripts/btc_daily_2010_2017.csv";
const OUT_PATH = "registry/voraui/btc-rainbow-chart/btc-seed.ts";

const csv = readFileSync(CSV_PATH, "utf8");
const rows = [];
for (const line of csv.split("\n")) {
  const trimmed = line.trim();
  if (!trimmed || trimmed.startsWith("#") || trimmed.startsWith("date,")) continue;
  const [date, close] = trimmed.split(",");
  if (!date || !close) continue;
  const ts = Date.parse(`${date}T00:00:00Z`);
  const price = Number(close);
  if (!Number.isFinite(ts) || !Number.isFinite(price)) continue;
  rows.push([Math.floor(ts / 1000), price]);
}
rows.sort((a, b) => a[0] - b[0]);

const body = rows.map(([t, p]) => `  [${t}, ${p}],`).join("\n");
writeFileSync(
  OUT_PATH,
  `// Generated by scripts/generate-btc-seed.mjs. Do not edit by hand.\n` +
    `// BTC daily closes 2010-07-18 .. 2017-08-16 as [unixSeconds, closeUsd].\n` +
    `// Source: Coin Metrics community data (historical closes never change).\n` +
    `export const BTC_DAILY_SEED: ReadonlyArray<readonly [number, number]> = [\n${body}\n];\n`,
);
console.log(`Wrote ${rows.length} rows to ${OUT_PATH}`);
